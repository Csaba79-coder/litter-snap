package com.csaba79coder.littersnap.value;

/**
 * This enum contains the litter status values used in the application.
 */
public enum LitterStatus {

    /**
     * Litter status values.
     * <p>
     *     REPORTED: the litter was reported
     *     COMPLETED: the litter was cleaned up
     *     PROCESSING: the litter is being processed
     *     NOT_FOUND: the litter was not found
     */
    REPORTED,
    COMPLETED,
    PROCESSING,
    NOT_FOUND
}